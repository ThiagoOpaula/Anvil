mod common;

use clap::Parser;

use anvil::cli::{Cli, Command};

#[test]
fn cli_no_args_defaults() {
    let cli = Cli::parse_from(["anvil"]);
    assert!(cli.mods_dir.is_none());
    assert!(matches!(cli.command, None));
}

#[test]
fn cli_mods_dir_global() {
    let cli = Cli::parse_from(["anvil", "--mods-dir", "/custom/mods"]);
    assert_eq!(
        cli.mods_dir,
        Some(std::path::PathBuf::from("/custom/mods"))
    );
}

#[test]
fn cli_update_default_subcommand() {
    let cli = Cli::parse_from(["anvil", "update"]);
    assert!(matches!(cli.command, Some(Command::Update(_))));
}

#[test]
fn cli_update_with_flags() {
    let cli = Cli::parse_from([
        "anvil", "update",
        "--game-version", "1.21.1",
        "--loader", "fabric",
        "--include", "sodium*",
        "--exclude", "old-*",
        "--verbose",
        "--dry-run",
        "--no-backup",
        "--max-updates", "5",
        "--yes",
        "--changelog",
    ]);
    match cli.command {
        Some(Command::Update(args)) => {
            assert_eq!(args.common.game_version.as_deref(), Some("1.21.1"));
            assert_eq!(args.common.loader.as_deref(), Some("fabric"));
            assert_eq!(args.common.include, vec!["sodium*"]);
            assert_eq!(args.common.exclude, vec!["old-*"]);
            assert!(args.common.verbose);
            assert!(args.common.dry_run);
            assert!(args.no_backup);
            assert_eq!(args.max_updates, Some(5));
            assert!(args.yes);
            assert!(args.changelog);
        }
        _ => panic!("expected Update variant"),
    }
}

